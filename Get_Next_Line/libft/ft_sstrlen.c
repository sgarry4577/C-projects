#include "libft.h"

int		ft_sstrlen(int n)
{
	int f;
	int d;

	f = 0;
	d = -1;
	if (n < 0)
	{
		f++;
		n *= d;
	}
	while (n >= 10)
	{
		n /= 10;
		f++;
	}
	return (f);
}
