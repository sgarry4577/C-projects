#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n == -2147483648 || n == 2147483647)
	{
		if (n == -2147483648)
			ft_putstr_fd("-2147483648", fd);
		if (n == 2147483647)
			ft_putstr_fd("2147483647", fd);
	}
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		c = n % 10 + '0';
		write(fd, &c, 1);
	}
}
