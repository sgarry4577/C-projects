#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s1[a])
		a++;
	while (s2[i])
	{
		s1[a + i] = s2[i];
		i++;
	}
	s1[a + i] = '\0';
	return (s1);
}
