#include "libft.h"

int		ft_strsearch(char const *str, char c)
{
	size_t i;

	i = 0;
	while (str[i] && str[i] != c)
		i++;
	if (str[i] == '\0' && c != '\0')
		return (-1);
	return (i);
}
