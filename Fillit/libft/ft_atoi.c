#include "libft.h"

int		ft_atoi(const char *str)
{
	int			i;
	long int	g;
	int			f;

	i = 0;
	f = 1;
	g = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			f = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		g *= 10;
		if (g < 0)
			return ((f < 0) ? 0 : -1);
		g += str[i] - '0';
		i++;
	}
	g *= f;
	return (g);
}
