#include "ft_printf.h"

int		get_after_dot_float(char *c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (c[i] != '.')
		i++;
	i++;
	while (c[i])
	{
		i++;
		j++;
	}
	return (j);
}
