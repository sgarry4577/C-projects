int		ft_iterative_factorial(int nb)
{
	int f;

	if (nb == 1 || nb == 0)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	f = 1;
	while (nb > 0)
	{
		f *= nb;
		nb--;
	}
	return (f);
}
