int		ft_sqrt(int nb)
{
	int i;
	int f;

	i = 0;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	f = nb / 2;
	while (i <= f)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
