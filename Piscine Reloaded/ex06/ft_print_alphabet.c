void	ft_putchar(char c);

void	ft_print_alphabet(void)
{
	char c;
	char i;
	char s;

	c = 'a';
	i = 0;
	while (i < 26)
	{
		s = c + i;
		ft_putchar(s);
		i++;
	}
}
